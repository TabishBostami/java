public class solid_rhombus {
    public static void main(String[] args) {
        int n = 5;
        for(int k =1;k<=n;k++){

        
        for(int i=1;i<(2*n)-k;i++){
            System.out.print(" ");
        }
        for(int j=0;j<n;j++){
            System.out.print("*");
        }

        System.out.println("");
    }
    
  }
}
